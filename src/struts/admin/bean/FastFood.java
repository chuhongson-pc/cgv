package struts.admin.bean;

public class FastFood {
	private String maFF;
	private String tenFF;
	private String loaiFF;
	private String moTa;
	private String giaTien;
	private int trangThai;
	
	
	public String getGiaTien() {
		return giaTien;
	}

	public void setGiaTien(String giaTien) {
		this.giaTien = giaTien;
	}

	public int getTrangThai() {
		return trangThai;
	}

	public void setTrangThai(int trangThai) {
		this.trangThai = trangThai;
	}

	public String getTenFF() {
		return tenFF;
	}

	public void setTenFF(String tenFF) {
		this.tenFF = tenFF;
	}

	public String getMaFF() {
		return maFF;
	}

	public void setMaFF(String maFF) {
		this.maFF = maFF;
	}

	public String getLoaiFF() {
		return loaiFF;
	}

	public void setLoaiFF(String loaiFF) {
		this.loaiFF = loaiFF;
	}

	public String getMoTa() {
		return moTa;
	}

	public void setMoTa(String moTa) {
		this.moTa = moTa;
	}

}
