package struts.admin.bean;

public class Poster {

	private String maPoster;
	private String poster;
	private String link;
	private String moTa;
	
	
	

	public String getMoTa() {
		return moTa;
	}

	public void setMoTa(String moTa) {
		this.moTa = moTa;
	}

	public String getMaPoster() {
		return maPoster;
	}

	public void setMaPoster(String maPoster) {
		this.maPoster = maPoster;
	}

	public String getPoster() {
		return poster;
	}

	public void setPoster(String poster) {
		this.poster = poster;
	}

	public String getLink() {
		return link;
	}

	public void setLink(String link) {
		this.link = link;
	}

}
