package struts.bean;

public class Seat {
	private String maPhong;
	private String toaDoX;
	private String toaDoY;
	private String maGhe;
	private int loaiGhe;

	public String getMaPhong() {
		return maPhong;
	}

	public void setMaPhong(String maPhong) {
		this.maPhong = maPhong;
	}

	public String getToaDoX() {
		return toaDoX;
	}

	public void setToaDoX(String toaDoX) {
		this.toaDoX = toaDoX;
	}

	public String getToaDoY() {
		return toaDoY;
	}

	public void setToaDoY(String toaDoY) {
		this.toaDoY = toaDoY;
	}

	public String getMaGhe() {
		return maGhe;
	}

	public void setMaGhe(String maGhe) {
		this.maGhe = maGhe;
	}

	public int getLoaiGhe() {
		return loaiGhe;
	}

	public void setLoaiGhe(int loaiGhe) {
		this.loaiGhe = loaiGhe;
	}

}
